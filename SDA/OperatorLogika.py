# AND (Akan mengembalikan nilai false jika salah satunya bernilai false)
print("===AND===")
print("Nilai True dan True adalah:", True and True)
print("Nilai False dan True adalah:", False and True)
print("Nilai True dan False adalah:", True and False)
print("Nilai False dan False adalah:", False and False)

# OR (Akan mengembalikan nilai true jika salah satunya bernilai true)
print("===OR===")
print("Nilai True dan True adalah:", True or True)
print("Nilai False dan True adalah:", False or True)
print("Nilai True dan False adalah:", True or False)
print("Nilai False dan False adalah:", False or False)

# NOT (Akan mengembalikan nilai sebaliknya/negasi)
print("===NOT===")
print("Hasil dari True adalah:", not True)
print("Hasil dari False adalah:", not False)


