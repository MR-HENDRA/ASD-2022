# TIPE DATA FLOAT (angka dengan koma)
data_float = 12.5
print("data float = ",data_float)
print(" - bertipe = ",type(data_float))

# TIPE DATA STRING (Kumpulan karakter)
data_string = "Hendra"
print("nama = ",data_string)
print(" - bertipe = ",type(data_string))

# TIPE DATA BOOLEAN (Data Biner True/False)
data_boolean = True
print("Boolean = ",data_boolean)
print(" - bertipe = ",type(data_boolean))